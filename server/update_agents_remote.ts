async function updateAgentsRemote() {
  console.log("Обновление названий агентов через Supabase...");
  
  for (const update of agentUpdates) {
    try {
      const { error } = await supabase
        .from('agents')
        .update({
          name: update.name,
          description: update.description,
          useCase: update.useCase,
          tags: update.tags,
        })
        .eq('id', update.id);
      
      if (error) {
        console.error(`❌ Ошибка при обновлении агента ${update.name}:`, error);
      } else {
        console.log(`✅ Обновлен агент: ${update.name}`);
      }
    } catch (error) {
      console.error(`❌ Ошибка при обновлении агента ${update.name}:`, error);
    }
  }
  
  console.log("✅ Обновление агентов завершено!");
}

// Запуск обновления
updateAgentsRemote().catch(console.error);
