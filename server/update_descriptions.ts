async function updateDescriptions() {
  console.log("Обновление описаний агентов...");
  
  for (const update of descriptionUpdates) {
    try {
      await db
        .update(agents)
        .set({
          description: update.description,
        })
        .where(eq(agents.id, update.id));
      
      console.log(`✅ Обновлено описание: ${update.id}`);
    } catch (error) {
      console.error(`❌ Ошибка при обновлении описания ${update.id}:`, error);
    }
  }
  
  console.log("✅ Обновление описаний завершено!");
}

// Запуск обновления
